# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

notaPrimeiro = int(input('Digite a nota do primeiro bimestre: '))
notaSegundo = int(input('Digite a nota do segundi bimestre: '))
notaTerceiro = int(input('Digite a nota do terceiro bimestre: '))
notaQuarta = int(input('Digite a nota do quarto bimestre: '))

media = (notaPrimeiro + notaSegundo + notaTerceiro + notaQuarta) / 4

print(media)
